def iter_action_mode_specs() -> tuple[WanActionModeSpec, ...]:
    """Return specs in stable display order."""
    return tuple(WAN_ACTION_MODE_SPECS[mode] for mode in WanActionMode)


def get_action_mode_spec(mode: WanActionMode | str) -> WanActionModeSpec:
    """Resolve a mode enum or value to its spec."""
    if not isinstance(mode, WanActionMode):
        mode = WanActionMode(mode)
    return WAN_ACTION_MODE_SPECS[mode]
