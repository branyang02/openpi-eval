"""Explicit Wan-to-action inference mode contracts.

The modes here describe what an action inference path requires from Wan, not a
specific training script implementation. Keep this module lightweight so tests
and reporting CLIs can depend on the contract without importing model code.
"""

from __future__ import annotations

from dataclasses import dataclass
from enum import Enum
from typing import Any


class WanActionMode(str, Enum):
    """Supported high-level Wan action inference styles."""

    DECODED_VIDEO_IDM = "decoded_video_idm"
    CURRENT_WAN_PREFIX_ACTION_EXPERT = "current_wan_prefix_action_expert"
    PARTIAL_WAN_PREFIX_ACTION_EXPERT = "partial_wan_prefix_action_expert"


@dataclass(frozen=True, slots=True)
class WanActionModeSpec:
    """Machine-testable contract for one Wan action inference style."""

    mode: WanActionMode
    name: str
    summary: str
    wan_inputs: tuple[str, ...]
    wan_process: str
    action_decoder: str
    action_inputs: tuple[str, ...]
    runs_wan_generation: bool
    generates_video: bool
    produces_future_latents: bool
    consumes_future_pixels: bool
    uses_wan_prefix_tokens: bool
    can_decode_for_visual_debugging: bool
    pi05_style_current_prefix_reuse: bool
    exposes_reusable_action_memory: bool
    native_wan_attention_kv_cache: bool
    frozen_wan: bool
    future_latent_slots: str
    debug_decode: str
    memory_contract: str

    def to_dict(self) -> dict[str, Any]:
        """Return a JSON-serializable description of this contract."""
        return {
            "mode": self.mode.value,
            "name": self.name,
            "summary": self.summary,
            "wan_inputs": list(self.wan_inputs),
            "wan_process": self.wan_process,
            "action_decoder": self.action_decoder,
            "action_inputs": list(self.action_inputs),
            "runs_wan_generation": self.runs_wan_generation,
            "generates_video": self.generates_video,
            "produces_future_latents": self.produces_future_latents,
            "consumes_future_pixels": self.consumes_future_pixels,
            "uses_wan_prefix_tokens": self.uses_wan_prefix_tokens,
            "can_decode_for_visual_debugging": self.can_decode_for_visual_debugging,
            "pi05_style_current_prefix_reuse": self.pi05_style_current_prefix_reuse,
            "exposes_reusable_action_memory": self.exposes_reusable_action_memory,
            "native_wan_attention_kv_cache": self.native_wan_attention_kv_cache,
            "frozen_wan": self.frozen_wan,
            "future_latent_slots": self.future_latent_slots,
            "debug_decode": self.debug_decode,
            "memory_contract": self.memory_contract,
        }


